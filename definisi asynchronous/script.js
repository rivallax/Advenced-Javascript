//     Single-threaded:

// Dalam model single-threaded, hanya ada satu thread eksekusi yang berjalan pada satu waktu tertentu.
// Semua tugas dieksekusi secara berurutan, satu per satu.
// Jika ada tugas yang membutuhkan waktu lama untuk dieksekusi, maka eksekusi program akan terhenti sampai tugas tersebut selesai.

// Multi-threaded:

// Dalam model multi-threaded, program dapat memiliki lebih dari satu thread eksekusi yang berjalan secara bersamaan.
// Setiap thread dapat menangani tugas-tugas yang berbeda secara independen.
// Thread-thread ini dapat bekerja secara bersamaan, bahkan jika satu thread mengalami penundaan dalam menangani tugas tertentu, thread-thread lainnya masih dapat berjalan tanpa terpengaruh.


